package com.bjxc.school;

public class Graduation {
	private String studentnum;
	private String stuname;
	private Integer subjectId;
	private Integer parttime;
	private Integer partmile;
	private Integer mtime;
	private Integer mmile;
	private String checkresult;
	public String getStudentnum() {
		return studentnum;
	}
	public void setStudentnum(String studentnum) {
		this.studentnum = studentnum;
	}
	public String getStuname() {
		return stuname;
	}
	public void setStuname(String stuname) {
		this.stuname = stuname;
	}
	public Integer getSubjectId() {
		return subjectId;
	}
	public void setSubjectId(Integer subjectId) {
		this.subjectId = subjectId;
	}
	public Integer getParttime() {
		return parttime;
	}
	public void setParttime(Integer parttime) {
		this.parttime = parttime;
	}
	public Integer getPartmile() {
		return partmile;
	}
	public void setPartmile(Integer partmile) {
		this.partmile = partmile;
	}
	public Integer getMtime() {
		return mtime;
	}
	public void setMtime(Integer mtime) {
		this.mtime = mtime;
	}
	public Integer getMmile() {
		return mmile;
	}
	public void setMmile(Integer mmile) {
		this.mmile = mmile;
	}
	public String getCheckresult() {
		return checkresult;
	}
	public void setCheckresult(String checkresult) {
		this.checkresult = checkresult;
	}
	
}
